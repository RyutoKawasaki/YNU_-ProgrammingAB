#include <stdio.h>

int main(void) {
  int n, n_100, n_010, n_001;
  printf("3桁の整数を入力してください\n");
  scanf("%d", &n);

  n_100 = n / 100;
  n_010 = n / 10;
  n_010 = n_010 % 10;
  n_001 = n % 10;

  printf("100の位 = %3d\n10の位 = %3d\n1の位 = %3d\n", n_100, n_010, n_001);

  return 0;
}

